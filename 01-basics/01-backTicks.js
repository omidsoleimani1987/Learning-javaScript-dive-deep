// we can use backtick to define a string too

// javascript template literal
let myVariable = 'omid';
console.log(`say hello to ${myVariable}`);
//we can add line breaks, line breaks are part of string:
console.log(`say hello 
                to ${myVariable}`);
// another way:
let newVariable = 'goodbye \n' + 'omidsoleimani';
console.log(newVariable);
console.log(`say hello to \n ${myVariable}`);

