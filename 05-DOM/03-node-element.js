/*
difference between Nodes and Elements

Nodes:
    - the objects that make up the DOM
    - every thing in the DOM is a node
    - HTML tags are 'element node' or just 'element'
    - text creates 'text node'
    - attributes creates 'attribute node'

Elements:
    - elements are one type of nodes
    - elements are just nodes which are created of rendered html tags  not the text in there
    - there are special properties and methods to interact with the elements
    - can be selected in different ways
    - can be created and removed via javaScript
*/
